package org.csstudio.utility.ldap.treeconfiguration;

import java.io.File;
import java.io.IOException;


import org.eclipse.core.runtime.FileLocator;
import org.eclipse.core.runtime.Platform;
import org.osgi.framework.Bundle;
import org.osgi.framework.BundleActivator;
import org.osgi.framework.BundleContext;

/**
 * Activator.
 *
 * @author bknerr
 * @since 01.07.2011
 */
public class TreeConfigurationActivator implements BundleActivator {

    /**
     * The id of this Java plug-in (value <code>{@value}</code> as defined in MANIFEST.MF.
     */
    public static final String PLUGIN_ID = "org.csstudio.utility.ldap.treeconfiguration";

    private static TreeConfigurationActivator INSTANCE;

    /**
     * Don't instantiate.
     * Called by framework.
     */
    public TreeConfigurationActivator() {
        if (INSTANCE != null) {
            throw new IllegalStateException("TreeModelActivator " + PLUGIN_ID + " does already exist.");
        }
        INSTANCE = this; // Antipattern is required by the framework!
    }

    /*
     * (non-Javadoc)
     * @see org.osgi.framework.BundleActivator#start(org.osgi.framework.BundleContext)
     */
    @Override
    public void start(final BundleContext context) throws Exception {
        // EMPTY
    }

    /*
     * (non-Javadoc)
     * @see org.osgi.framework.BundleActivator#stop(org.osgi.framework.BundleContext)
     */
    @Override
    public void stop(final BundleContext context) throws Exception {
        // EMPTY
    }

    static String getResourceFromBundle(final String dtdFilePath) throws IOException {
        final Bundle bundle = Platform.getBundle(TreeConfigurationActivator.PLUGIN_ID);
        final File loc = FileLocator.getBundleFile(bundle);
        return  new File(loc, dtdFilePath).toString();
    }
}
